    private void bEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bEditActionPerformed
     if(bEdit.getText()== "bEdit"){
         textNpm.requestFocus();
     }else{
         String npm = textNpm.getText();
         String nama = textNama.getText();
         String alamat = textAlamat.getText();
         String kelas = (String)comboBoxKelas.getSelectedItem();
         String jk = (String)comboBoxKelas.getSelectedItem();
     
        try {
             Statement st = (Statement) konek.getConnection().createStatement();
             st.executeUpdate("update dt_mahasiswa set npm ='"+npm+"',"+"nama='"+nama+"',"+"kelas='"+kelas+"',"+"jk='"+jk+"',"+"alamat='"+alamat+"'" + "where npm ='"+npm+"' or nama='"+nama+"'");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Data Gagal Diubah");
        }
        datatable();
    }//GEN-LAST:event_bEditActionPerformed
    }
