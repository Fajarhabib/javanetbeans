 private void datatable(){
            DefaultTableModel tbl = new DefaultTableModel();
            tbl.addColumn("NPM");
            tbl.addColumn("Nama");
            tbl.addColumn("Kelas");
            tbl.addColumn("Jenis Kelamin");
            tbl.addColumn("Alamat");
            tabel.setModel(tbl);
            try {
                   Statement st = (Statement) konek.getConnection().createStatement();
                    ResultSet rs = st.executeQuery("select * from dt_mahasiswa");
                    while (rs.next())
                    {
                        tbl.addRow(new Object[]{
                        rs.getString("npm"),
                        rs.getString("nama"),
                        rs.getString("kelas"),
                        rs.getString("jk"),
                        rs.getString("alamat"),
                        });
                    tabel.setModel(tbl);
                    }
            }catch( Exception e){
                JOptionPane.showMessageDialog(null, "Database Tidak Konek");
            }
        }
