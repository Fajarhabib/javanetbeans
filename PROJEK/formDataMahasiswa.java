    private void bSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bSimpanActionPerformed
        String npm = textNpm.getText();
        String nama = textNama.getText();
        String kelas = (String) comboBoxKelas.getSelectedItem();
        String jk = (String) comboBoxJenisKelamin.getSelectedItem();
        String alamat = textAlamat.getText();
        try {
            Statement st = (Statement) konek.getConnection().createStatement();
            st.executeUpdate("insert into dt_mahasiswa values('"+npm+"','"+nama+"','"+kelas+"','"+jk+"','"+alamat+"')");
            st.close();
             JOptionPane.showMessageDialog(null, "Data berhasil disimpan");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Data gagal disimpan");
        }
        datatable();
    }//GEN-LAST:event_bSimpanActionPerformed

    private void bHapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bHapusActionPerformed
        String npm = textNpm.getText();
        String nama = textNama.getText();
        try {
            Statement st = (Statement) konek.getConnection().createStatement();
            st.executeUpdate("delete from dt_mahasiswa where npm='"+npm+"' or nama='"+nama+"'");
            JOptionPane.showMessageDialog(null, "Data Berhasil Dihapus");
              textNpm.setText("");
                textNama.setText("");
                comboBoxKelas.setSelectedItem("A");
                 comboBoxJenisKelamin.setSelectedItem("Laki-Laki");
                textAlamat.setText("");
                textNpm.requestFocus();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Data Gagal Dihapus");
        }
        datatable();
    }//GEN-LAST:event_bHapusActionPerformed

    private void bCariActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bCariActionPerformed
        try {
            Statement st = (Statement) konek.getConnection().createStatement();
            ResultSet rs = st.executeQuery("select * from dt_mahasiswa where "+"npm like '%"+textCari.getText()+"%' or "+"nama like '%"+textCari.getText()+"%'");
            DefaultTableModel tbl = new DefaultTableModel();
            tbl.addColumn("npm");
            tbl.addColumn("nama");
            tbl.addColumn("kelas");
            tbl.addColumn("jk");
            tbl.addColumn("alamat");
            
            tabel.setModel(tbl);
            while (rs.next()){
                tbl.addRow(new Object[]{
                rs.getString("npm"),
                rs.getString("nama"),
                rs.getString("kelas"),
                rs.getString("jk"),
                rs.getString("alamat"),
            });
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(bEdit, e);
        }
    }//GEN-LAST:event_bCariActionPerformed

    private void tabelMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabelMouseClicked
        int i = tabel.getSelectedRow();
        if(i==-1){
            return;
        }
        String npm= (String)tabel.getValueAt(i, 0);
        textNpm.setText(npm);
        String nama= (String)tabel.getValueAt(i, 1);
        textNama.setText(nama);
        String alamat= (String)tabel.getValueAt(i, 4);
        textAlamat.setText(alamat);
        String kelas= (String) tabel.getValueAt(i, 2);
        comboBoxKelas.setSelectedItem(kelas);
        String jk= (String) tabel.getValueAt(i, 3);
        comboBoxJenisKelamin.setSelectedItem(jk);
       
    }//GEN-LAST:event_tabelMouseClicked

